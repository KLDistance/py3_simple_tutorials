import numpy as np

my_var_val1 = 100
my_var_val2 = 40

print(my_var_val1 + my_var_val2)
print(my_var_val1 / my_var_val2)
print(my_var_val1 // my_var_val2)
print(my_var_val1 % my_var_val2)
print(my_var_val1 ** 3)
print(np.sqrt(my_var_val1))
print(my_var_val1 ** 0.5)

print(int(my_var_val1 ** 0.5))
print('%.7f' % float(my_var_val1))