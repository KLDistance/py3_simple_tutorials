my_list = [100, 200, 300, 400, 500]

# find 3rd element from list
print(my_list[2])

# change 1st element
my_list[0] = 999
print(my_list)

# delete 2nd element in list
del my_list[1]
print(my_list)

# append element from tail
my_list.append(333)
print(my_list)

# concatenate lists
print(my_list + [3, 2, 3])

# repeat list
print(my_list * 3)

